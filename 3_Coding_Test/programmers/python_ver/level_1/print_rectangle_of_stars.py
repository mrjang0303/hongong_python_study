# 문제: 프로그래머스 Lv.1 '직사각형 별찍기'
# 설명: 두 개의 정수 n과 m을 입력받아, 가로 n, 세로 m인 직사각형을 별(*)로 출력하는 코드
# 링크: https://school.programmers.co.kr/learn/courses/30/lessons/12969

# 풀이: 
# 1. 표준 입력을 통해 공백으로 구분된 두 정수를 한 줄에서 읽고, map()과 언패킹을 이용해 n과 m 변수에 저장합니다.
# 2. 세로 길이인 m만큼 반복하는 for문을 사용하여 직사각형의 행을 만듭니다.
# 3. 반복문 안에서 문자열 곱셈("*")과 가로 길이 n을 사용하여 각 행에 필요한 별의 개수를 만듭니다.
# 4. print() 함수는 자동으로 줄 바꿈을 해주기 때문에, 추가적인 코드 없이도 원하는 직사각형 형태를 출력할 수 있습니다.

n, m = map(int, input().strip().split(' '))

for i in range(m):
    print("*" * n)