# 문제: 프로그래머스 Lv.1 '최대공약수와 최소공배수'
# 설명: 두 수를 입력받아 두 수의 최대공약수와 최소공배수를 반환하는 함수, solution을 완성해 보세요. 
#       배열의 맨 앞에 최대공약수, 그다음 최소공배수를 넣어 반환하면 됩니다.
#       예를 들어 두 수 3, 12의 최대공약수는 3, 최소공배수는 12이므로 solution(3, 12)는 [3, 12]를 반환해야 합니다.
# 링크: https://school.programmers.co.kr/learn/courses/30/lessons/12940

# 풀이
# 1. import math를 사용하여 파이썬의 math 모듈을 가져옵니다. 이 모듈에는 수학 관련 함수들이 미리 구현되어 있어, 직접 코드를 작성하지 않아도 됩니다.
# 2. math.gcd(n, m) 함수를 사용하여 두 수 n과 m의 **최대공약수(GCD)**를 구합니다. 이 함수는 유클리드 호제법이 적용되어 있어 매우 빠르게 결과를 반환합니다.
# 3. 최소공배수(LCM) = (두 수의 곱) / (최대공약수)라는 수학 공식을 활용하여 최소공배수를 구합니다. 이때, 몫만 구하는 정수 나누기 연산자 //를 사용하여 결과가 정수가 되도록 합니다.
# 4. 마지막으로, 문제에서 요구하는 대로 [최대공약수, 최소공배수] 형태의 배열을 만들어 반환합니다.

import math

def solution(n, m):
    # 1. 최대공약수를 구하고
    divisor = math.gcd(n, m)
    
    # 2. 공식을 이용해 최소공배수를 구한다.
    multiple = (n * m) // divisor
    
    return [divisor, multiple]