# 문제: 프로그래머스 Lv.1 '문자열 내 마음대로 정렬하기'
# 설명: 문자열 리스트 strings와 정수 n이 주어졌을 때, 각 문자열의 n번째 인덱스 글자를 기준으로 먼저 정렬하고, n번째 글자가 같으면 전체 문자열을 기준으로 사전순 정렬하는 문제입니다.
# 링크: https://school.programmers.co.kr/learn/courses/30/lessons/12915

# 풀이:
# 1. 파이썬의 sorted() 함수를 사용하여 strings 리스트를 정렬합니다.
# 2. 정렬 기준(key)으로 람다 함수를 사용합니다.
# 3. 람다 함수는 각 문자열(s)을 받아 (s[n], s) 형태의 튜플을 반환합니다.
# 4. 파이썬은 튜플을 정렬할 때 첫 번째 요소(s[n])를 먼저 비교하고, 첫 번째 요소가 같으면 두 번째 요소(s)를 비교하는 규칙을 따릅니다.
# 5. 이 방식으로 n번째 인덱스 글자 기준의 정렬과, 동일한 경우 문자열 전체의 사전순 정렬을 동시에 구현합니다.

def solution(strings, n):
    return sorted(strings, key = lambda s: (s[n], s))