# 문제: 프로그래머스 Lv.0 '두 수의 곱 구하기'
# 설명: 정수 num1, num2가 매개변수 주어집니다. num1과 num2를 곱한 값을 return 하도록 solution 함수를 완성해주세요.
# 링크: https://school.programmers.co.kr/learn/courses/30/lessons/120804
# 풀이: num1을 num2로 곱한 후, 그 값을 answer 변수 값으로 설정한 후, answer를 return 함.

def solution(num1, num2):
    answer = num1 * num2
    return answer